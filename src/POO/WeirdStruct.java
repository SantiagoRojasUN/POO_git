public class WeirdStruct {
    private final long data_A;
    private final long data_B;
    private final long data_C;
    private final long data_D;

    public WeirdStruct(long data_A, long data_B, long data_C, long data_D) {
        this.data_A = data_A;
        this.data_B = data_B;
        this.data_C = data_C;
        this.data_D = data_D;
    }

    public long getData_A() {
        return data_A;
    }

    public long getData_B() {
        return data_B;
    }

    public long getData_C() {
        return data_C;
    }

    public long getData_D() {
        return data_D;
    }
    
    
    
}
